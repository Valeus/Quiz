package com.example.a10019356.quizproject;

public class TrueAndFalse {

    String q,t,f,s;

    public TrueAndFalse(String q, String t, String f, String s){
        this.q=q;
        this.t=t;
        this.f=f;
        this.s=s;
    }

    public String getS() {
        return s;
    }

    public String getF() {
        return f;
    }

    public String getQ() {
        return q;
    }

    public String getT() {
        return t;
    }
}
